void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	int		i;
	char	*m;
	char	*l;

	i = 0;
	m = (char*)dst;
	l = (char*)src;
	while (i < n)
	{
		m[i] = l[i++];
	}
	return (dst);
}
